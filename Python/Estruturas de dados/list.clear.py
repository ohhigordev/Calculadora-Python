"""
Em Python, 'list.clear()' é um método usado para remover todos os elementos de uma lista.
Ele limpa a lista, deixando-a vazia. Aqui está uma explicação detalhada:
"""
'''
list: Este é o nome da lsita qie você deseja limpar, ou seja, remover todos os seus elementos.
clear: É o método usado para realizar a operção de limpeza na lista. Quando você chaama o 
'list.clear(), todos os elementos dentro da lista são removidos, deixando-a vazia.
'''

my_list = [1, 2, 3, 4, 5]

# Limpando a lista
my_list.clear()
print('lista após a limpeza:', my_list)
